package com.headless.ecommerce.domain;
public class UrlFacts {
    private String url;
    private String facts;
    private String pics_repo;
    private String api_repo;
    
    public String getUrl() {
        return url;
    }
    public void setUrl(String url) {
        this.url = url;
    }
    public String getFacts() {
        return facts;
    }
    public void setFacts(String facts) {
        this.facts = facts;
    }
    public String getPics_repo() {
        return pics_repo;
    }
    public void setPics_repo(String pics_repo) {
        this.pics_repo = pics_repo;
    }
    public String getApi_repo() {
        return api_repo;
    }
    public void setApi_repo(String api_repo) {
        this.api_repo = api_repo;
    }
    
    

}
